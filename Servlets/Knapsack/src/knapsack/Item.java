package knapsack;

/**
 *
 * @author rav4
 */
public class Item {
    private String id;
    private int precio;

    public Item(String id, int precio) {
        this.id = id;
        this.precio = precio;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }
}
